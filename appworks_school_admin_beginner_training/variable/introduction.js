// assign value: integer
// variable name can be arbitrary
let a = 1
let b = 2

// can't multiple create variable with same name
let a = 3 // error

// but we can change the value of exist variable
a = 3

// assign value: string
let say_something = "hello world"
console.log(say_something)

// assign value: add two variables
let c = a + b
console.log(c)

// re-assign value
c = c + 1
console.log(c)

c = c - a
console.log(c)

// special notation
c += 1
console.log(c)
c ++
console.log(c)
